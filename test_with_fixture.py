# ============================================================================

def pytest_sessionfinish(session: pytest.Session) -> None:
    """
    Функция Pytest, которая выполняется после завершения всех тестов.
    Выводит итоговое сообщение.

    Args:
        session: Объект сессии Pytest
    """

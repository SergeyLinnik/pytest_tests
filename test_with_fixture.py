"""
Файл с тестами, использующими фикстуру в качестве аргумента.
Фикстура содержит yield для выполнения действий после теста.
"""

import pytest


@pytest.fixture
def set_up() -> None:
    """
    Фикстура, которая выполняется перед и после каждым тестом.
    Использует yield для разделения настройки и завершения.

    Действия до yield: вход в систему
    Действия после yield: выход из системы
    """
    # --------------------------------------------------------------------
    # ДЕЙСТВИЯ ДО ЗАПУСКА ТЕСТА (SETUP)
    # --------------------------------------------------------------------
    print("\n🔐 Вход в систему выполнен")

    # --------------------------------------------------------------------
    # ПЕРЕДАЧА УПРАВЛЕНИЯ ТЕСТУ
    # --------------------------------------------------------------------
    yield

    # --------------------------------------------------------------------
    # ДЕЙСТВИЯ ПОСЛЕ ЗАВЕРШЕНИЯ ТЕСТА (TEARDOWN)
    # --------------------------------------------------------------------
    print("🚪 Выход из системы выполнен")


def test_sending_mail_1(set_up: None) -> None:
    """
    Тест отправки письма #1.
    Использует фикстуру set_up как аргумент.

    Ожидаемый вывод:
    - Вход в систему выполнен
    - Письмо #1 отправлено
    - Выход из системы выполнен
    """
    print("📧 Письмо #1 отправлено")


